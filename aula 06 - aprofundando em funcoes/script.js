// tipos de função

// declarativas
function funcao() {
  console.log("Função declarativa aqui");
}

funcao();

// expressão de função
// com nomeação
var funcao = function () {
  console.log("Sou uma msg de função de expressão");
};

funcao();

// arrow function
var funcao = () => {
  console.log("Funcao Arrow Function");
};

funcao();
